<?php
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Адрес, на который будет отправлено сообщение
    $to = "sarvan.shiriev@mail.ru";

    // Тема сообщения
    $subject = "Простое сообщение с кнопки SEND";

    // Текст сообщения
    $message = "Это простое сообщение, отправленное с помощью кнопки SEND.";

    // Отправляем письмо
    if (mail($to, $subject, $message)) {
        echo "Сообщение успешно отправлено.";
    } else {
        echo "Ошибка при отправке сообщения.";
    }
}
?>
